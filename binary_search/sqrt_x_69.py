class Solution:
    def mySqrt(self, x: int) -> int:
        left, right = 0, x
        ans = 0

        while left <= right:
            mid = (left + right) // 2

            if mid * mid <= x:
                ans = mid          # store possible answer
                left = mid + 1     # try bigger
            else:
                right = mid - 1    # too big

        return ans
